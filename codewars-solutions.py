#return even or odd
def even_or_odd(number):
    if number % 2 == 0: return "even"
    else: return "odd"

#return integer as a string
def number_to_string(num):
    # Return a string of the number here!
    return str(num)

#remove all whitespace from string
def no_space(x):
    newx = x.replace(" ", "")
    return newx
